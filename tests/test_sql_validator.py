class TestExtractReferencedTables(unittest.TestCase):

    def test_single_table(self):
        tables = extract_referenced_tables("SELECT * FROM users")
        self.assertEqual(set(tables), {"users"})

    def test_join_table(self):
        tables = extract_referenced_tables(
            "SELECT * FROM users JOIN orders ON users.id = orders.user_id"
        )
        self.assertEqual(set(tables), {"users", "orders"})

    # --- LLM FAILURE CASE ---
    # The LLM's first implementation used a simple FROM regex that only
    # captured the first table name after FROM.  This test caught the bug.
    def test_extract_comma_separated_tables(self):
        tables = extract_referenced_tables("SELECT * FROM users, orders")
        self.assertEqual(
            set(tables), {"users", "orders"},
            "Comma-separated FROM tables must all be extracted"
        )

    def test_three_comma_separated_tables(self):
        tables = extract_referenced_tables("SELECT * FROM users, orders, products")
        self.assertEqual(set(tables), {"users", "orders", "products"})

    def test_multiple_joins(self):
        sql = (
            "SELECT * FROM orders "
            "JOIN users ON orders.user_id = users.id "
            "JOIN products ON orders.product_id = products.id"
        )
        tables = extract_referenced_tables(sql)
        self.assertEqual(set(tables), {"orders", "users", "products"})

    def test_table_alias_ignored(self):
        # Alias 'u' must not appear in results — 'users' should
        tables = extract_referenced_tables("SELECT u.name FROM users u")
        self.assertIn("users", tables)
        self.assertNotIn("u", tables)

    def test_table_as_alias_ignored(self):
        tables = extract_referenced_tables("SELECT u.name FROM users AS u")
        self.assertIn("users", tables)
        self.assertNotIn("u", tables)

    def test_no_from_clause_returns_empty(self):
        tables = extract_referenced_tables("SELECT 1 + 1")
        self.assertEqual(tables, [])

    def test_case_insensitive_from(self):
        tables = extract_referenced_tables("select * from users")
        self.assertEqual(set(tables), {"users"})

    def test_names_are_lowercased(self):
        tables = extract_referenced_tables("SELECT * FROM Users")
        self.assertIn("users", tables)


