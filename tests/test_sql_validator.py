class TestValidateQuery(unittest.TestCase):

    def setUp(self):
        self.db_path = _make_db(["users", "orders"])

    def tearDown(self):
        os.unlink(self.db_path)

    def test_valid_select_passes(self):
        result = validate_query("SELECT * FROM users", self.db_path)
        self.assertTrue(result["valid"])
        self.assertIsNone(result["error"])

    def test_valid_join_passes(self):
        result = validate_query(
            "SELECT * FROM users JOIN orders ON users.id = orders.user_id",
            self.db_path,
        )
        self.assertTrue(result["valid"])

    def test_nonexistent_table_fails(self):
        result = validate_query("SELECT * FROM invoices", self.db_path)
        self.assertFalse(result["valid"])
        self.assertIn("invoices", result["error"])

    def test_insert_fails(self):
        result = validate_query("INSERT INTO users VALUES (1)", self.db_path)
        self.assertFalse(result["valid"])

    def test_semicolon_injection_fails(self):
        result = validate_query(
            "SELECT * FROM users; DROP TABLE users", self.db_path
        )
        self.assertFalse(result["valid"])

    def test_comment_injection_fails(self):
        result = validate_query(
            "SELECT * FROM users -- bypass", self.db_path
        )
        self.assertFalse(result["valid"])

    def test_result_has_required_keys(self):
        result = validate_query("SELECT 1", self.db_path)
        self.assertIn("valid", result)
        self.assertIn("error", result)


