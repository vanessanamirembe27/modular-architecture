def _make_db(tables):
    """
    Create a temporary SQLite database with the given tables and return its path.

    Args:
        tables (list[str]): Table names to create (schema doesn't matter for
                            validator tests — existence is all that counts).

    Returns:
        str: Path to the temp database file.
    """
    fd, path = tempfile.mkstemp(suffix=".db")
    os.close(fd)
    conn = sqlite3.connect(path)
    for table in tables:
        conn.execute(
            f"CREATE TABLE {table} (id INTEGER PRIMARY KEY AUTOINCREMENT, name TEXT)"
        )
    conn.commit()
    conn.close()
    return path


