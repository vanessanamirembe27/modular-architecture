class TestCheckTablesExist(unittest.TestCase):

    def setUp(self):
        self.db_path = _make_db(["users", "orders"])

    def tearDown(self):
        os.unlink(self.db_path)

    def test_existing_table_passes(self):
        valid, error = check_tables_exist(["users"], self.db_path)
        self.assertTrue(valid)
        self.assertIsNone(error)

    def test_multiple_existing_tables_pass(self):
        valid, error = check_tables_exist(["users", "orders"], self.db_path)
        self.assertTrue(valid)

    def test_nonexistent_table_is_blocked(self):
        valid, error = check_tables_exist(["ghosts"], self.db_path)
        self.assertFalse(valid)
        self.assertIn("ghosts", error)

    def test_mix_of_existing_and_missing_is_blocked(self):
        valid, error = check_tables_exist(["users", "ghosts"], self.db_path)
        self.assertFalse(valid)
        self.assertIn("ghosts", error)

    def test_empty_table_list_passes(self):
        # No tables referenced (e.g. SELECT 1+1) — nothing to check
        valid, error = check_tables_exist([], self.db_path)
        self.assertTrue(valid)

    def test_table_name_case_insensitive(self):
        valid, error = check_tables_exist(["USERS"], self.db_path)
        self.assertTrue(valid)


