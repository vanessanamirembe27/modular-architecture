class TestCheckNoInjectionPatterns(unittest.TestCase):

    def test_clean_query_passes(self):
        valid, error = check_no_injection_patterns("SELECT id, name FROM users")
        self.assertTrue(valid)
        self.assertIsNone(error)

    def test_line_comment_is_blocked(self):
        valid, error = check_no_injection_patterns("SELECT * FROM users -- comment")
        self.assertFalse(valid)
        self.assertIn("--", error)

    def test_block_comment_open_is_blocked(self):
        valid, error = check_no_injection_patterns("SELECT /* trick */ * FROM users")
        self.assertFalse(valid)

    def test_block_comment_close_is_blocked(self):
        valid, error = check_no_injection_patterns("SELECT * FROM users */")
        self.assertFalse(valid)

    def test_semicolon_is_blocked(self):
        # Prevents multi-statement injection: SELECT 1; DROP TABLE users
        valid, error = check_no_injection_patterns("SELECT * FROM users; DROP TABLE users")
        self.assertFalse(valid)
        self.assertIn(";", error)

    def test_exec_keyword_is_blocked(self):
        valid, error = check_no_injection_patterns("SELECT EXEC('xp_cmdshell')")
        self.assertFalse(valid)

    def test_xp_prefix_is_blocked(self):
        valid, error = check_no_injection_patterns("SELECT xp_cmdshell FROM users")
        self.assertFalse(valid)

    def test_where_clause_with_operators_passes(self):
        # Normal WHERE clauses must not be blocked
        valid, error = check_no_injection_patterns(
            "SELECT * FROM users WHERE age > 18 AND name = 'Alice'"
        )
        self.assertTrue(valid)


