"""
Unit tests for app/validator/sql_validator.py

Test authorship note
--------------------
These tests were designed by the developer — NOT generated by an LLM.
They define the expected behavior of the validator and serve as the
specification that any implementation (including LLM-assisted code) must satisfy.

LLM failure case documented here
---------------------------------
When the LLM initially implemented extract_referenced_tables it produced:

    pattern = r'\\bFROM\\s+([a-zA-Z_][a-zA-Z0-9_]*)'

This regex only captures the *first* identifier after FROM.
It fails for comma-separated multi-table queries such as:

    SELECT * FROM users, orders

test_extract_comma_separated_tables caught the failure.
The implementation was refined to split the FROM clause on commas
and extract the first token from each segment.
"""

