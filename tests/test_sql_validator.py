class TestCheckQueryType(unittest.TestCase):

    def test_select_is_allowed(self):
        valid, error = check_query_type("SELECT * FROM users")
        self.assertTrue(valid)
        self.assertIsNone(error)

    def test_select_lowercase_is_allowed(self):
        valid, error = check_query_type("select id from users")
        self.assertTrue(valid)
        self.assertIsNone(error)

    def test_insert_is_blocked(self):
        valid, error = check_query_type("INSERT INTO users VALUES (1, 'a')")
        self.assertFalse(valid)
        self.assertIn("INSERT", error)

    def test_update_is_blocked(self):
        valid, error = check_query_type("UPDATE users SET name='x' WHERE id=1")
        self.assertFalse(valid)

    def test_delete_is_blocked(self):
        valid, error = check_query_type("DELETE FROM users WHERE id=1")
        self.assertFalse(valid)

    def test_drop_is_blocked(self):
        valid, error = check_query_type("DROP TABLE users")
        self.assertFalse(valid)

    def test_create_is_blocked(self):
        valid, error = check_query_type("CREATE TABLE foo (id INTEGER)")
        self.assertFalse(valid)

    def test_empty_query_is_blocked(self):
        valid, error = check_query_type("")
        self.assertFalse(valid)

    def test_whitespace_only_is_blocked(self):
        valid, error = check_query_type("   ")
        self.assertFalse(valid)


